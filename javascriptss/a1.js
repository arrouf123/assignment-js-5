
// for a1
document.getElementById('A1').addEventListener('click',function(){
    const ticketsPrice = 550;
    const previousTotalPrice = document.getElementById('total-price').innerText
    const currentPriceInt = parseInt(previousTotalPrice);
    const currentPrice = currentPriceInt + ticketsPrice
    
    // setElementById('total-price',currentPrice)
    console.log(currentPriceInt);
    document.getElementById('total-price').innerText = currentPrice

    // EntryElement('K. Accessories')
    // visibleCouponButton()
    // visibleMakePurchaseBtn()
    console.log(previousTotalPrice)

   
})

